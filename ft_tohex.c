#include "ft_printf.h"
void ft_tohex(int d)
{
  int r, c =0;
  char hd[100];
  while (d != 0)
  {
    r = d%16;
    if(r<10)
      hd[c++] = 48 + r;
    else
      hd[c++]=55 +r;
    d = d/16;
  }
  while(--c >= 0)
  {
      ft_putchar(hd[c]);
  }
}
